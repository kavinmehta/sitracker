<p>Global Signature:
A signature to insert at the bottom of outgoing emails. It's recommended that
you begin this signature with two dashes, a space and a line feed.</p>

<p>Multiple global signatures can be defined one of which will be choosen at random to be inserted at the bottom of each outgoing email.</p>

<p>Remember that any changes here will be effective immediately and outgoing emails with the
&lt;globalsignature&gt; placeholderwill carry the new signature.</p>

