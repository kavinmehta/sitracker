<P>Global Signature:
A signature to insert at the bottom of outgoing emails. It's recommended that<br />
you begin this signature with two dashes, a space and a line feed.</p>

<p>Remember that any changes here will be effective immediately and outgoing emails will carry<br />
the new signature.</P>

<p>Edit the content of the Text Box with the chosen signature and the click on <i>Update Signature</i>.</p>

