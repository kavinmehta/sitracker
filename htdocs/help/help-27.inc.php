<p>To book a day off, follow these steps</p>
<ul>
  <li>From the menu click on one of the following options depending upon the type of day off you need:<BR>
  <ul>
    <li>Holiday</li>
    <li>Sickness</li>
    <li>Working Away</li>
    <li>Training</li>
  </ul>
  <li>Click on the day you want to take off</li>
  <ul>
    <li>It's possible to modify the selection by clicking on<br />
    <ul>
      <li><i>the morning</i>,</li>
      <li><i>the afternoon</i> or</li>
      <li><i>full day</i></li>
    </ul>
      so you can change from full day to half day or vice versa</li>
  </ul>
  <li>Once you have booked all your days, click on <i>Send holiday request</i> to be approved by your line manager</li>
  <li>Select your line manager from the list</li>
  <li>Type in any comments if applicable</li>
  <li>Finally, click on <i>Submit</i> to send the request.</li>
</ul>

<p><strong>NOTE:</strong> If the day you are trying to book doesn't change its background colour to white once clicked, means
that it has been already booked by you as either Hols, Sickness, Working Away or Training.</p>

