<P>This page shows a list of all users along with their contact details</P>



<P>This page can show Instant Messenger addresses for<br>

<ul>

<li>AOL Instant Messenger (AIM)</li>

<li>ICQ Messenger</li>

<li>MSN Instant Messenger (Windows Messenger)</li>

</ul>

<br>

You must have the appropriate messenger configured in your own profile for the relevant columns to be displayed.  For example if

you have only AOL and MSN addresses configured in your profile then only the AOL and MSN columns will be shown.  To display the 

ICQ column you will need to configure your own ICQ address in your profile.





</P>

