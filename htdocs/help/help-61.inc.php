<P><B>The <i>View</i> options are,</b></P>
<ul>
  <li><i>Expand All:</i> Shows every possible information</li>
  <li><i>Visible Only:</i>What the customer can see</li>
  <li><i>Hideen Only:</i>What the customer can't see</li>
  <li><i>All with auto updates:</i></li>
  <li><i>Colapse All:</i>Compacts the information at maximum. Only headers are shown</li>
</ul>
<p><b>Hints:</b><br />
<li>By double-clicking in the contact details, the Support Incident Details are also shown</li><br />
<li>By double-clicking in any incident header, it will collapse/expand its content</li></p>



