<P>To <i>Add a Quotation</i> follow the next steps</P>

<ul>
  <li>Select today's <i>Date</i></li>
  <li>Choo se a <i>Currency</i> from the drop down box</li>
  <li>Type in the person's name who is <i>Preparing</i> the quotation</li>
  <li>Write who this quotation is addressed <i>To</i>. Only one name per line</li>
  <li>Indicate the <i>Customer Ref</i></li>
  <li>Specify the <i>Number of Pages</i></li>
  <li>If applicable, briefly enumerate any <i>Special Remarks</i></li>
  <li>Tick <i>Accept Purchase Order</i> box if applicable</li>
  <li>List of Quoted Items. Per each <i>Item</i> denote,</li>
  <ul>
    <li><i>Quantity</i></li>
    <li><i>Item Description</i></li>
    <li>Total <i>Cost</i></li>    
  </ul>
  <li>List of Optional Quoted Items. If any per each <i>Item</i> denote,</li> 
  <ul>
    <li><i>Quantity</i></li>
    <li><i>Item Description</i></li>
    <li>Total <i>Cost</i></li>
  </ul>
  <li>Eventually, click on the <i>Submit Query</i> button</li>
</ul>

