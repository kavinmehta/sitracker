<P>To change the user's permissions, do the following</P>

<ul>
  <li>Select a <i>User Name</i> from the drop down list</li>
  <li>Click on <i>Continue</i></li>
  <li>If a box is ticked, the user will have that persimission</li>
  <li>When finished, click on <i>Make Changes</i></li>
</ul>

<p><b>NOTE:</b>A user must have had each permission set at least once, so that permissions records<br />
can be set up correctly.
 



