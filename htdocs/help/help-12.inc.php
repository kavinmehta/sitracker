<P>There are two ways of searching for the contact you wish to view</P>

<P><strong>1.</strong> Enter the name of the contact you wish to view in the blank field box</P>
<P><strong>2.</strong> click on the appropriate letter for the contacts surname in the alphabetical listing, this will bring up the appropriate page.  Then find the contact you are looking for in the list.</P>

<P>To view contact details click on the contact name</P>
<P>To view contacts site details click on the site name</P>
<P>To create a new incident for the contact click on add incident</P>

