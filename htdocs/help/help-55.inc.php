<p>To delete a contact,</p>
<ul>
  <li>Select it from the drop down list</li>
  <li>Click on <em>Continue</em></li>
</ul>

<p>Should the delete contact have any incidents and/or contracts, you<br />
must assign them to another contact. To do so, select the receptor contact and click<br />
on <em>Delete</em>. If there is no contracts assigned to it, just click on <em>Delete</em></p>

<p>After a brief confirmation page, is then shown before you can edit the details<br />
of the receptor contact.</p>

