
<strong>To add a new contact:</strong>
<P>Please check that the contact does not already exist before adding as a new contact.</P>
<P>The categories with an asterisk * are mandatory and need to be completed or the system will not process the new contact.</P>
<P>Please use the other areas of the form to log all available and relevant detail about the new contact.</P>
<P>The three data protection boxes are to be used if the contact does not want to be contacted in one of the specified ways. i.e. if the contact does not want to receive phone communications, or the information to be used in reports, then click in the relevant box and this will be accomplished.</P>
<P>Click on the add button at the bottom of the screen to complete the add contact process</P>


