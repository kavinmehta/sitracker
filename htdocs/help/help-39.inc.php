<p>In order to add a new Contract, follow the following steps<br />
NOTE: Mandatory fields are marked with an asterisk (*)</p>
<ul>
  <li>Pick up a <em>Site Name*</em> from the list</li>
  <li>Select the <em>Product*</em> to be maintained</li>
  <li>Choose one of the <em>Resellers*</em></li>
  <li>Enter the number of <em>Licences*</em></li>
  <li>Select the <em>Licence Type*</em></li>
  <li>Enter a <em>Expiry Date*</em> for the Contract</li>
  <li>Pick up a <em>Service Level</em> from the list</li>
  <li>Select a <em>Incident Pool</em></li>
  <li>Choose the <em>Contact Person*</em> within the selected <em>Site</em></li>
</ul>
