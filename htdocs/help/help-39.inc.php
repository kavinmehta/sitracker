<p>In order to add a new Contract, follow the following steps<br />
NOTE: Mandatory fields are marked with an asterisk (*)</p>
<ul>
  <li>Pick up a <i>Site Name*</i> from the list</li>
  <li>Select the <i>Product*</i> to be maintained</li>
  <li>Choose one of the <i>Resellers*</i></li>
  <li>Enter the number of <i>Licences*</i></li>
  <li>Select the <i>Licence Type*</i></li>
  <li>Enter a <i>Expiry Date*</i> for the Contract</li>
  <li>Pick up a <i>Service Level</i> from the list</li>
  <li>Select a <i>Incident Pool</i></li>
  <li>Choose the <i>Contact Person*</i> within the selected <i>Site</i></li>
</ul>



